import 'package:supabase_flutter/supabase_flutter.dart';
import '../models/action_item_model.dart';
import 'action_repository.dart';

class SupabaseActionRepository implements ActionRepository {
  final SupabaseClient _client = Supabase.instance.client;

  @override
  Future<List<ActionItemModel>> fetchActionItems({String? responsibleRole}) async {
    try {
      var query = _client
          .from('action_items')
          .select()
          .filter('resolved_at', 'is', null);

      if (responsibleRole != null) {
        query = query.eq('responsible_role', responsibleRole);
      }

      final response = await query.order('created_at', ascending: false);
      return (response as List).map((e) => ActionItemModel.fromJson(e)).toList();
    } catch (e) {
      print("Error fetching action items: $e");
      return [];
    }
  }

