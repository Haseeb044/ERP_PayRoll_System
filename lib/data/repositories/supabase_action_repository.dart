  @override
  Future<void> resolveAction(String actionId, {String? notes}) async {
    try {
      await _client.from('action_items').update({
        'resolved_at': DateTime.now().toIso8601String(),
        'resolved_by': _client.auth.currentUser?.id,
        'resolution_notes': notes ?? 'Resolved by system',
      }).eq('id', actionId);
    } catch (e) {
      print("Error resolving action item: $e");
      throw e;
    }
  }

  @override
  Future<void> createActionItem(ActionItemModel item) async {
    try {
      final json = item.toJson();
      json.remove('id'); // ID is serial/UUID generated by DB
      json['responsible_role'] = 'accountant'; // Ensure responsible_role is set to 'accountant' as required by RLS policy
      await _client.from('action_items').insert(json);
    } catch (e) {
      print("Error creating action item: $e");
